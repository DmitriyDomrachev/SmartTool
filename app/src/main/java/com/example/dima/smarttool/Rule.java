public class Rule {
    long id;
    long condition;


    public Rule(long id, long condition) {
        this.id = id;
        this.condition = condition;
    }

    public long getId() {
        return id;
    }

    public void setId(long id) {
        this.id = id;
    }

    public long getCondition() {
        return condition;
    }

    public void setCondition(long condition) {
        this.condition = condition;
    }




    @Override
    public String toString() {
        return "Contact{" +
                "id=" + id +
                ", condition='" + condition + '\'' +
                '}'+"\n";
    }
}

